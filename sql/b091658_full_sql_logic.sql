  values (
    safe_cache_key,
    safe_payload,
    now(),
    now() + make_interval(mins => safe_ttl)
  )
  on conflict (cache_key) do update
  set
    feed_payload = excluded.feed_payload,
    generated_at = excluded.generated_at,
    expires_at = excluded.expires_at,
    updated_at = now();
end;
$$;

